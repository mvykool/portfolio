import Head from "next/head";
import React, { useState } from "react";
import Footer from "./Footer";
import Navbar from "./Navbar";
import useMediaQuery from "../hooks/useMediaQuery";
import { useEffect } from "react";
import ScrollToTop from "./ScrollTop";

interface Props {
  children: any;
}

const Layout = ({ children }: Props) => {
  const [selectedPage, setSelectedPage] = useState("home");
  const isAboveMediumScreens = useMediaQuery("(min-width: 1060px)");
  const [topPage, setTopPage] = useState(true);
  const [theme, setTheme] = useState("dark");

  useEffect(() => {
    // On mount, read the theme from localStorage or system preference
    const savedTheme = localStorage.getItem("theme");
    const systemTheme = window.matchMedia("(prefers-color-scheme: dark)")
      .matches
      ? "dark"
      : "light";
    const initialTheme = savedTheme || systemTheme;

    setTheme(initialTheme);
    document.documentElement.classList.toggle("dark", initialTheme === "dark");
  }, []);

  useEffect(() => {
    const handleScroll = () => {
      if (window.screenY === 0) setTopPage(true);
      if (window.screenY !== 0) setTopPage(false);
    };
    window.addEventListener("scroll", handleScroll);
    return () => window.removeEventListener("scroll", handleScroll);
  }, []);

  return (
    <>
      <Head>
        <title>Michael H | Portfolio</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="mh.png" />
      </Head>

      <Navbar
        selectedPage={selectedPage}
        setSelectedPage={setSelectedPage}
        page={undefined}
        topPage={topPage}
      />
      <main className="min-h-screen flex flex-col">
        <div className="w-11/12 relative md:w-full mx-auto my-2 md:my-0 overflow-x-hidden flex-grow">
          {children}
        </div>
        <ScrollToTop />
        <div className="mt-auto">
          {" "}
          {/* Remove relative and block, add mt-auto */}
          <Footer />
        </div>
      </main>
    </>
  );
};

export default Layout;
